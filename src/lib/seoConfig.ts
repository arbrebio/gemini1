export function generatePageSEO(page: string, lang: string = 'en') {
  const pageConfig = seoPages[page as keyof typeof seoPages];
  if (!pageConfig) return null;

  const langConfig = pageConfig[lang as keyof typeof pageConfig];
  if (!langConfig) return pageConfig['en'];

  return langConfig;
}

export function generateLocationKeywords(location: string, baseKeywords: string[]) {
  return baseKeywords.map(keyword => [
    `${keyword} ${location}`,
    `${keyword} in ${location}`,
    `${location} ${keyword}`
  ]).flat();
}
